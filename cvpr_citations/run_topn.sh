#!/usr/bin/env bash
# Top-N 引用論文の抽出
# 実行: bash run_topn.sh
# パラメータはこのファイルの変数を直接編集して変更する

set -euo pipefail

# ============================================================
# パラメータ設定
# ============================================================
TOP_N=10          # 取得する上位件数
# ============================================================

cd "$(dirname "$0")"

echo "=== CVPR Citation Analyzer — Top-${TOP_N} 抽出 ==="
uv run python topn.py --top "$TOP_N"
echo "=== 完了 ==="
